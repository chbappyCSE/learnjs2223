/* Operations 
    Arithmetic Operators
    =====================
    + Addition
    - Subtraction
    * Multiplication
    / Division
    ** Exponentiation (ES 2016)
    % Modulus (Division Remainder)
    ++ Increment
    -- Decrement
    
    Assignment Operator
    ===================
    Operator                            Example     Same As
    = (Assignment)                      x = y       x = y
    += (Addition Assignment)            x += y      x = x + y
    -= (Subtraction Assignment)         x -= y      x = x - y
    *= (Multiplication Assignment)      x *= y      x = x * y
    /= (Division Assignment)            x /= y      x = x / y
    %= (Modulus Assignment)             x %=y       x = x % y
    **= (Exponentiation Assignment)     x **= y     x= x ** y

    String Operator
    ===============
    + (Concatenate) The + operator can also be used to add (concate) string
    += The += operator can also be used to add (concate) string
    
    Comparison Operator
    ===================
    ==      Equal to
    ===     Equal value and equal type
    !=      Not Equal
    !==     Not equal value or not equal type
    >       Greater than
    <       less than
    >=      Greter than or equal to
    <=      Less than or equal to
    ?       Ternary operator

    Logical Operator
    ================
    &&      Logical and
    ||      Logical or
    !       Logical not

    Type Operator
    ===============
    typeof          Return the type of a variable
    instanceof      Return true if an object is an instance of object type.

    Bitwise operator
    =================
    ** Bit operators work on 32 bits numbers.
    Operator    Description     Example     Same as         Result      Decimal
    &           AND             5 & 1       0101 & 0001     0001        1
    |           OR              5 | 1       0101 | 0001     0101        5
    ~           NOT             ~ 5         ~ 0101          1010        10
    ^           XOR             5 ^ 1       0101 ^ 0001     0100        4
    <<          Zero fill left shift 5 << 1 0101 << 1       10101       10
    >>          Signed right shift   5 >> 1 0101 >> 1       0010        2
    >>>         Zero fill righ shift 5 >>> 1    0101 >>> 1  0101        2
*/